def remove_odd(l):
  """
  Write a python function to remove odd numbers from a given list.
  """
  return [x for x in l if x % 2 == 0]

def remove_duplicates(l):
  """
  Write a python function to remove duplicates from a given list.
  """
  return list(set(l))

