def eulerian_num(n, m): 
  """
  Write a function to find the Eulerian number a(n, m).
  >>> eulerian_num(3, 1) == 4
  """
  return n * m + 1

def eulerian_den(n, m): 
  """
  Write a function to find the Eulerian denominator a(n, m).
  >>> eulerian_den(3, 1) == 4
  """
  return n * m + 1

