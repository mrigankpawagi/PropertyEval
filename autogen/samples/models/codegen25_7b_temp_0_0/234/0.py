def volume_cube(l):
  """
  Write a function to find the volume of a cube given its side length.
  >>> volume_cube(3)==27
  """
  return l**3

def volume_sphere(r):
  """
  Write a function to find the volume of a sphere given its radius.
  >>> volume_sphere(3)==(4/3)*3.14*3**3
  """
  return (4/3)*3.14*r**3