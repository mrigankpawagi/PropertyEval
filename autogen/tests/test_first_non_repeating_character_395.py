str1 = text(alphabet='abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ', min_size=1, max_size=SMALL_SEQUENCE_LEN)

strategy = str1
if not isinstance(strategy, tuple):
    strategy = (strategy,)

def first_non_repeating_character(str1):
  char_order = []
  ctr = {}
  for c in str1:
    if c in ctr:
      ctr[c] += 1
    else:
      ctr[c] = 1 
      char_order.append(c)
  for c in char_order:
    if ctr[c] == 1:
      return c
  return None

@given(tuples(*strategy))
def test_fuzz(args):
    run_with_timeout(0.3, first_non_repeating_character, *args)
